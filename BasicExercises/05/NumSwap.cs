using System;

public class NumSwap{
	public static void Main(){
		
		int firstNum, secondNum;

		System.Console.Write("\nInput First Number: ");
		firstNum = int.Parse(Console.ReadLine());
		System.Console.Write("\nInput Second Number: ");
		secondNum = int.Parse(Console.ReadLine());
		
		System.Console.Write("\nFirst Number: "+secondNum);
		System.Console.Write("\nSecond Number: "+firstNum);

		System.Console.Write("\n");
	}
}
